const locales = ["es", "de", "fr", "hi", "ja", "zh", "ta"];

for (const locale of locales) {
  const filePath = path.join(localesDir, locale, "common.json");
  const data = JSON.parse(fs.readFileSync(filePath, "utf8"));
  if (data.investmentPortfolio) {
    console.log(locale, "already has investmentPortfolio, skipping");
    continue;
  }
  data.investmentPortfolio = investmentPortfolioByLocale[locale];
  const str = JSON.stringify(data);
  fs.writeFileSync(filePath, str, "utf8");
  console.log("Added investmentPortfolio to", locale);
}

